function parseQualityBands() {
  console.log('\n  Parsing quality quantization bands...')
  
  const queryFile = join(EXTRACTED_DATA, QUALITY_QUANTIZATION_FILE)
  if (!existsSync(queryFile)) {
    console.log('  Quality quantization file not found - run extract-game-data.ps1')
    console.log('  Or manually run: starbreaker dcb query "CraftingQualityQuantizationRecord"')
    return { bands: {}, distribution: {} }
  }
  
  const rawContent = readFileSync(queryFile, 'utf-8')
  
  // CRITICAL: First strip out ALL lines that start with "---" 
  // StarBreaker interleaves these markers IN THE MIDDLE of JSON content
  const content = rawContent
    .split('\n')
    .filter(line => !line.trim().startsWith('---'))
    .join('\n')
  
  // Parse the concatenated JSON objects from query output
  // Format: {record1}{record2}...
  const bands = {}
  
  // Split by }{ boundary and reconstruct each JSON object
  const records = content.split(/\}\s*\{/).map((r, i, arr) => {
    if (i === 0) return r + '}'
    if (i === arr.length - 1) return '{' + r
    return '{' + r + '}'
  }).filter(r => r.includes('CraftingQualityQuantizationRecord'))
  
  for (const recordStr of records) {
    try {
      // Clean up the string - remove any trailing non-JSON content
      let cleanStr = recordStr.trim()
      const lastBrace = cleanStr.lastIndexOf('}')
      if (lastBrace > 0) {
        cleanStr = cleanStr.substring(0, lastBrace + 1)
      }
      
      const record = JSON.parse(cleanStr)
      if (!record._RecordName_) continue
      
      // Extract resource name from record name
      const match = record._RecordName_.match(/Quantization_(\w+)$/)
      if (!match) continue
      
      const resourceName = match[1].toLowerCase()
      if (resourceName === 'template') continue // Skip template
      
      const quantization = record._RecordValue_?.qualityQuantization
      if (!quantization?.bands) continue
      
      // Extract band mapped values (these are the threshold values)
      const bandValues = quantization.bands.map(b => b.mappedValue)
      
      // Also store the full band data for reference
      bands[resourceName] = {
        name: match[1],
        bands: quantization.bands.map(b => ({
          start: b.start,
          end: b.end,
          mappedValue: b.mappedValue
        })),
        // The mapped values in order (for qualityBands.ts format)
        thresholds: bandValues
      }
    } catch (e) {
      // Skip malformed records
      continue
    }
  }
  
  console.log(`  Parsed ${Object.keys(bands).length} resource quality bands`)
  
  // Parse quality distribution if available
  const distribution = {}
  const distFile = join(EXTRACTED_DATA, QUALITY_DISTRIBUTION_FILE)
  if (existsSync(distFile)) {
    const rawDistContent = readFileSync(distFile, 'utf-8')
    // Strip --- markers that may be interleaved
    const distContent = rawDistContent
      .split('\n')
      .filter(line => !line.trim().startsWith('---'))
      .join('\n')
    const distRecords = distContent.split(/\}\s*\{/).map((r, i, arr) => {
      if (i === 0) return r + '}'
      if (i === arr.length - 1) return '{' + r
      return '{' + r + '}'
    }).filter(r => r.includes('CraftingQualityDistributionRecord'))
    
    for (const recordStr of distRecords) {
      try {
        let cleanStr = recordStr.trim()
        const lastBrace = cleanStr.lastIndexOf('}')
        if (lastBrace > 0) cleanStr = cleanStr.substring(0, lastBrace + 1)
        
        const record = JSON.parse(cleanStr)
        const match = record._RecordName_?.match(/QualityDistribution_(\w+)$/)
        if (!match) continue
        
        const typeName = match[1].toLowerCase()
        const dist = record._RecordValue_?.qualityDistribution
        if (!dist) continue
        
        distribution[typeName] = {
          name: match[1],
          type: dist._Type_,
          min: dist.min,
          max: dist.max,
          mean: dist.mean,
          stddev: dist.stddev
        }
      } catch (e) {
        continue
      }
    }
    console.log(`  Parsed ${Object.keys(distribution).length} quality distributions`)
  }
  
  return { bands, distribution }
}

