/**
 * Build compact blueprint acquisition premiums from blueprint-acquisition.json
 * for bundling into dfp-engine/formula.ts.
 */
import fs from 'fs'
import path from 'path'

const root = path.resolve(import.meta.dirname, '..')
const acquisitionPath = path.join(root, 'src', 'data', 'blueprint-acquisition.json')
const outPath = path.join(root, 'dfp-engine', 'acquisition-premiums.generated.ts')

function effortAuecForMinRep(minRep) {
  if (minRep <= 0) return 25_000
  if (minRep < 800) return 40_000
  if (minRep < 2200) return 50_000
  if (minRep < 5800) return 75_000
  if (minRep < 15000) return 100_000
  if (minRep < 38000) return 125_000
  return 150_000
}

function computeBlueprintAcquisitionPremium(bp) {
  if (bp.isAvailableByDefault) return 0
  const missions = bp.missionMatches
  if (!missions?.length) return 0

  let minCost = Infinity
  for (const m of missions) {
    const minRep = m.minReputation ?? 0
    const repPerRun = Math.max(1, m.repMin ?? m.repMax ?? 50)
    const dropChance = Math.max(0.01, m.dropChance ?? 1)
    const missionsToRep = minRep <= 0 ? 1 : Math.ceil(minRep / repPerRun)
    const expectedRuns = Math.ceil(missionsToRep / dropChance)
    const cost = expectedRuns * effortAuecForMinRep(minRep)
    if (cost < minCost) minCost = cost
  }

  return minCost === Infinity ? 0 : Math.round(minCost)
}

export function generateAcquisitionPremiums() {
  if (!fs.existsSync(acquisitionPath)) {
    fs.writeFileSync(
      outPath,
      `// AUTO-GENERATED — no blueprint-acquisition.json found\nexport const ACQUISITION_PREMIUMS: Record<string, number> = {}\nexport function getAcquisitionPremium(_file?: string): number { return 0 }\n`,
      'utf8'
    )
    console.log('acquisition-premiums: empty (no sidecar)')
    return
  }

  const data = JSON.parse(fs.readFileSync(acquisitionPath, 'utf8'))
  const premiums = {}

  for (const [file, bp] of Object.entries(data.blueprints ?? {})) {
    const premium = computeBlueprintAcquisitionPremium(bp)
    if (premium > 0) premiums[file] = premium
  }

  const lines = Object.entries(premiums)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([file, value]) => `  ${JSON.stringify(file)}: ${value},`)

  const content = `// AUTO-GENERATED by scripts/generate-acquisition-premiums.mjs — do not edit
// Source: src/data/blueprint-acquisition.json @ ${data.generatedAt ?? 'unknown'}

export const ACQUISITION_PREMIUMS: Record<string, number> = {
${lines.join('\n')}
}

export function getAcquisitionPremium(file?: string): number {
  if (!file) return 0
  return ACQUISITION_PREMIUMS[file] ?? 0
}
`

  fs.writeFileSync(outPath, content, 'utf8')
  console.log(
    `acquisition-premiums: ${Object.keys(premiums).length} non-zero entries (of ${Object.keys(data.blueprints ?? {}).length} reward BPs)`
  )
}

if (import.meta.url === `file://${process.argv[1].replace(/\\/g, '/')}`) {
  generateAcquisitionPremiums()
}
