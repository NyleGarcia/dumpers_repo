import fs from 'fs';
import path from 'path';

// Resolve paths relative to project root
const blueprintsPath = 'src/data/game-blueprints.json';
const goOutputPath = 'scripts/bp-dumper-go/mapping.go';
const pyOutputPath = 'scripts/bp-dumper-py/mapping.py';

if (!fs.existsSync(blueprintsPath)) {
  console.error(`Error: Blueprint database not found at ${blueprintsPath}`);
  process.exit(1);
}

console.log('Loading blueprint catalog...');
const rawData = fs.readFileSync(blueprintsPath, 'utf8');
const data = JSON.parse(rawData);

const mapping = {};
data.blueprints.forEach(bp => {
  if (bp.blueprintName && bp.internalName) {
    const name = bp.blueprintName.trim().toLowerCase();
    if (!mapping[name]) mapping[name] = [];
    if (!mapping[name].includes(bp.internalName)) {
      mapping[name].push(bp.internalName);
    }
  }
});

console.log(`Parsed ${Object.keys(mapping).length} unique blueprint display names.`);

// Generate Go file
let goContent = `// Code generated by scripts/generate-dumper-mappings.mjs. DO NOT EDIT.

package main

var blueprintNameToInternalNames = map[string][]string{
`;
for (const [name, ids] of Object.entries(mapping)) {
  const safeName = name.replace(/"/g, '\\"');
  const idsStr = ids.map(id => `"${id}"`).join(', ');
  goContent += `\t"${safeName}": {${idsStr}},\n`;
}
goContent += '}\n';

fs.writeFileSync(goOutputPath, goContent);
console.log(`Successfully generated Go mapping at ${goOutputPath}`);

// Generate Python file
let pyContent = `# Code generated by scripts/generate-dumper-mappings.mjs. DO NOT EDIT.

BLUEPRINT_NAME_TO_INTERNAL_NAMES = {
`;
for (const [name, ids] of Object.entries(mapping)) {
  const safeName = name.replace(/'/g, "\\'");
  const idsStr = ids.map(id => `'${id}'`).join(', ');
  pyContent += `    '${safeName}': [${idsStr}],\n`;
}
pyContent += '}\n';

fs.writeFileSync(pyOutputPath, pyContent);
console.log(`Successfully generated Python mapping at ${pyOutputPath}`);
