/**
 * Index ship-component grade + class from api.star-citizen.wiki for DFP ore retail anchoring
 * and blueprint card spec sublines.
 */
import fs from 'fs'
import path from 'path'

const API_BASE = 'https://api.star-citizen.wiki/api'
const RATE_MS = 1100
const root = path.resolve(import.meta.dirname, '..')
const blueprintsPath = path.join(root, 'src', 'data', 'Blueprints.json')
const outTsPath = path.join(root, 'dfp-engine', 'component-metadata.generated.ts')
const outJsonPath = path.join(root, 'src', 'data', 'component-metadata.json')
const cachePath = path.join(root, 'scripts', '.cache', 'sc-wiki-component-items.json')

const WIKI_TYPES = [
  'PowerPlant',
  'Shield',
  'Cooler',
  'QuantumDrive',
  'Radar',
  'TractorBeam',
  'SalvageHead',
  'FuelIntake',
  'WeaponMining',
  'WeaponGun',
  'WeaponMissile',
  'Bomb',
  'Turret',
]

let lastRequestAt = 0

async function apiFetch(urlPath) {
  const elapsed = Date.now() - lastRequestAt
  if (elapsed < RATE_MS) {
    await new Promise((r) => setTimeout(r, RATE_MS - elapsed))
  }
  lastRequestAt = Date.now()
  const url = urlPath.startsWith('http') ? urlPath : `${API_BASE}${urlPath}`
  const response = await fetch(url, { headers: { Accept: 'application/json' } })
  if (!response.ok) throw new Error(`SC Wiki API ${response.status}: ${url}`)
  return response.json()
}

async function paginateType(type) {
  const items = []
  let page = 1
  let lastPage = 1
  while (page <= lastPage) {
    const json = await apiFetch(
      `/items?filter[type]=${encodeURIComponent(type)}&page[number]=${page}&page[size]=50`,
    )
    items.push(...(json.data ?? []))
    lastPage = json.meta?.last_page ?? 1
    page++
  }
  return items
}

async function loadWikiItems(force) {
  if (!force && fs.existsSync(cachePath)) {
    try {
      const cached = JSON.parse(fs.readFileSync(cachePath, 'utf8'))
      if (Array.isArray(cached.items) && cached.items.length > 0) {
        const ageMs = Date.now() - (cached.fetchedAt ?? 0)
        if (ageMs < 7 * 24 * 60 * 60 * 1000) return cached.items
      }
    } catch {
      /* refresh */
    }
  }

  const all = []
  for (const type of WIKI_TYPES) {
    const batch = await paginateType(type)
    all.push(...batch)
    process.stdout.write(`  wiki ${type}: ${batch.length}\n`)
  }

  fs.mkdirSync(path.dirname(cachePath), { recursive: true })
  fs.writeFileSync(cachePath, JSON.stringify({ fetchedAt: Date.now(), items: all }, null, 0))
  return all
}

function normalizeClass(value) {
  if (!value || typeof value !== 'string') return null
  const key = value.toLowerCase().replace(/[^a-z]/g, '')
  if (key.includes('competition')) return 'competition'
  if (key.includes('civilian')) return 'civilian'
  if (key.includes('military')) return 'military'
  if (key.includes('industrial')) return 'industrial'
  if (key.includes('stealth')) return 'stealth'
  return key || null
}

function normalizeGrade(value) {
  if (!value || typeof value !== 'string') return null
  const g = value.trim().toUpperCase()
  return /^[ABCD]$/.test(g) ? g : null
}

function wikiItemToMeta(item) {
  const meta = {}
  const itemClass = normalizeClass(item.class)
  const grade = normalizeGrade(item.grade)
  if (itemClass) meta.itemClass = itemClass
  if (grade) meta.grade = grade
  return Object.keys(meta).length > 0 ? meta : null
}

function lookupWikiMeta(internalName, blueprintName, byClassLower, byNameLower) {
  const internal = (internalName ?? '').toLowerCase()
  if (internal && byClassLower.has(internal)) return byClassLower.get(internal)

  const withScitem = internal.endsWith('_scitem') ? internal : `${internal}_scitem`
  if (byClassLower.has(withScitem)) return byClassLower.get(withScitem)

  const nameKey = (blueprintName ?? '').trim().toLowerCase()
  if (nameKey && byNameLower.has(nameKey)) return byNameLower.get(nameKey)

  return null
}

export async function generateComponentMetadata({ force = false } = {}) {
  const catalog = JSON.parse(fs.readFileSync(blueprintsPath, 'utf8'))
  const indexedBps = catalog.blueprints.filter(
    (bp) =>
      bp.categoryName?.startsWith('Veh. Comp.') || bp.categoryName?.startsWith('Veh. Weapons'),
  )

  console.log('component-metadata: fetching wiki item index...')
  const wikiItems = await loadWikiItems(force)

  const byClassLower = new Map()
  const byNameLower = new Map()
  for (const item of wikiItems) {
    const meta = wikiItemToMeta(item)
    if (!meta) continue
    if (item.class_name) {
      byClassLower.set(item.class_name.toLowerCase(), meta)
    }
    if (item.name) {
      byNameLower.set(item.name.trim().toLowerCase(), meta)
    }
  }

  const byFile = {}
  let matched = 0
  for (const bp of indexedBps) {
    if (!bp.file) continue
    const meta = lookupWikiMeta(bp.internalName, bp.blueprintName, byClassLower, byNameLower)
    if (meta) {
      byFile[bp.file] = meta
      matched++
    }
  }

  const generatedAt = new Date().toISOString()

  const tsLines = [
    '// AUTO-GENERATED by scripts/generate-component-metadata.mjs — do not edit',
    `// Source: api.star-citizen.wiki @ ${generatedAt}`,
    '',
    'export interface ComponentMeta { itemClass?: string; grade?: string }',
    '',
    `export const COMPONENT_METADATA: Record<string, ComponentMeta> = ${JSON.stringify(byFile, null, 2)}`,
    '',
    'export function getComponentMetadata(',
    '  file?: string,',
    '  _internalName?: string,',
    '): ComponentMeta | null {',
    '  if (file && COMPONENT_METADATA[file]) return COMPONENT_METADATA[file]',
    '  return null',
    '}',
    '',
  ]

  fs.writeFileSync(outTsPath, tsLines.join('\n'))
  fs.writeFileSync(
    outJsonPath,
    JSON.stringify({ generatedAt, source: 'api.star-citizen.wiki', blueprints: byFile }, null, 2) + '\n',
  )
  console.log(`component-metadata: ${matched}/${indexedBps.length} veh blueprints matched`)
}

if (import.meta.url === `file://${process.argv[1].replace(/\\/g, '/')}`) {
  const force = process.argv.includes('--force')
  await generateComponentMetadata({ force })
}
